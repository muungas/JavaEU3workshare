package assignment.day6;

public class Question1_GallonToLiters {

	public static void main(String[] args) {
		
		double gallon,liter;
		
		gallon=15;
		liter =gallon*3.785412;
		
		System.out.println(gallon+" gallon is "+liter);
		
		float gallonf,literf;
		
		gallonf=15;
		literf =gallonf*3.785412f;
		
		System.out.println(gallonf+" gallon is "+literf);

	}

}
